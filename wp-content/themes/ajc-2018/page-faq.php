			<h3 class="gothic large-space-below">10. Are your diamonds conflict free?</h3>
			<p>Yes! Conflict diamonds or “blood diamonds” are illegally traded stones that were mined sometime in the last 50 years. These diamonds originated in countries such as Angola, Sierra Leone, Liberia, Cote d’Ivoire (Ivory Coast), Democratic Republic of the Congo (formerly Zaire) or The Republic of Congo, and were used to finance civil wars and deadly insurgencies. Because we only use antique and vintage diamonds that predate this period, our jewellery is completely conflict free. Initiatives such as the Kimberley Process Certification Scheme have been created in order to curb the dangerous blood diamond trade, however even this initiative is fraught with shortcomings. The only way to guarantee that a diamond is conflict free is to reuse vintage and antique diamonds that were mined prior to 1970...which is exactly what we do!</p>

