			<h3 class="gothic large-space-below">5. When was your jewellery made?</h3>
			<p>Our jewellery varies in age from the early 18th Century up until the 1960s and later. We have categorized our jewellery into different periods. Each period has many different designs and characteristics. These periods include Georgian, Victorian, Art Nouveau, Edwardian, Art Deco, Retro, Mid-Century and Modern.</p>
			<p><strong>Georgian Jewellery (1714-1830)</strong></p>
			<p>Georgian era jewellery is handmade, making the quality of each piece variable. Needless to say, Georgian jewellery is very rare. Often featuring nature-inspired designs such as leaves and birds, Georgian jewellery frequently includes precious stones.</p>
			<p><strong>Early Victorian, Romantic Jewellery (1839-1901)</strong></p>
			<p>Like jewellery of the Georgian era, early Victorian era jewellery features nature-inspired designs. Frequently, these designs would be delicately and intricately etched into gold. Lockets and brooches were popular everyday jewellery during the early Victorian era whereas colored gemstones and diamonds were worn during the evening.</p>
			<p><strong>Mid-Victorian, Grand Jewellery (1860-1880)</strong></p>
			<p>Because the Grand or Mid-Victorian era corresponded with the death of Queen Victoria&rsquo;s husband, many jewellery pieces have solemn, grave designs. Known as mourning jewellery, the pieces feature heavy, dark stones. Jet, onyx, amethyst, and garnet are frequently found in jewellery from this period. The jewellery also became especially creative during this period. More colorful designs were born featuring shells, mosaics and colorful gemstones.</p>
			<p><strong>Late Victorian, Aesthetic Jewellery (1885-1900)</strong></p>
			<p>During the Late Victorian or Aesthetic period, jewellers used diamonds and feminine, bright gemstones such as sapphire, peridot, and spinel. Star and crescent designs as well as elaborate hat pins were also popular.</p>
			<p><strong>Art Nouveau Jewellery (1890-1915)</strong></p>
			<p>Inspired by Rene Jules Lalique in France and other jewellers in America, Art Nouveau jewellery features natural designs such as flowers and butterflies.</p>
			<p><strong>Edwardian Jewellery (1901-1914)</strong></p>
			<p>The Edwardian period was born when Queen Victoria died and her son Edward became king. During this period, expensive gemstones such as diamonds, emeralds and rubies and elaborate designs were the fashion.</p>
			<p><strong>Art Deco Jewellery (1920-1935)</strong></p>
			<p>Influenced by African, Egyptian and Japanese themes, Art Deco jewellery is famous for its geometric designs, sharp lines, and bright colors. Bakelite, celluloid, eidl and doublets were used frequently during this era. Necklaces were long and bracelets were worn many at a time. Amber and murano beads were worn as well as chokers.</p>
			<p><strong>Retro Jewellery (1940s)</strong></p>
			<p>Inspired by Hollywood, Retro jewellery is colorful, bold and elaborate. Commonly worn were large cocktail rings, bracelets, watches, necklaces and charm bracelets.</p>

